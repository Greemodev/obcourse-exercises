package exercises;

public class If {
    public static void main(String[] args) {
        //exercises.If condition
        var numeroIf = 5;

        if (numeroIf > 0) {
            System.out.println("Positivo");
        } else if (numeroIf == 0){
            System.out.println("Es 0");
        } else {
            System.out.println("Negativo");
        }
    }
}
